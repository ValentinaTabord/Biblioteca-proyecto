    private void leerlibroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_leerlibroActionPerformed
        DefaultTableModel model = (DefaultTableModel) tablalibros.getModel();
        model.setRowCount(0);
        ArrayList<Libro> libros = new ArrayList();
        LibroController libro = new LibroController();
        List<Libro> leer = libro.leerLibros();
        libros.addAll(leer);

        Object columnas[] = new Object[6];
        for(Libro c : libros){
            columnas[0]=c.getId_libro();
            columnas [1]=c.getTitulo();
            columnas [2]=c.getAutor();
            columnas [3]=c.getEditorial();
            columnas [4]=c.getAnio_publicacion();
            columnas [5]=c.getIsbn();
            model.addRow(columnas);
        }

    }//GEN-LAST:event_leerlibroActionPerformed

